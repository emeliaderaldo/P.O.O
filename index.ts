// variaveis 
// let, const

let x = 10;
x = 30;
console.log(x);


const y = 20;
console.log(y);   // const = uma variavel constante, nao muda

// tipos
// number, string, boolean, number[], string[], any

let z:number = 30;
console.log(z);

let s:string = "emeli";
console.log(s);

let a:any = 30;
a = "tutu";
console.log(a);
